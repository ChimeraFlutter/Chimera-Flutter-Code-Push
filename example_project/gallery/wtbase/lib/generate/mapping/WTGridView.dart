class WTGridView extends WTVMBaseType<GridView> {
  static WTGridView? _instance;
  factory WTGridView() => _instance ??= WTGridView._internal();

  WTGridView._internal() {
    definePath = 'packages/flutter/lib/src/widgets/scroll_view.dart';
    defineName = 'GridView';

    attributesMap = {
      "GridView": m_GridView,
      "builder": builder,
      "custom": custom,
      "count": count,
      "extent": extent,
    };

    setAttributeMap = null;

    getAttributeMap = null;
